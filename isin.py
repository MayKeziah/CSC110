#Membership test
def isin(inlist, inelement):
    booly = False
    for elt in inlist:
        if elt == inelement:
            booly = True
    return booly

#When the chest strikes back!
def otherisin(inlist, inelement):
    for elt in inlist:
        if elt == inelement:
            return True
    return False

